def vector_2d_angle(v1, v2):
    v1_x = v1[0]
    v1_y = v1[1]
    v2_x = v2[0]
    v2_y = v2[1]
    try:
        # degrees将弧度转换为度，更加直观。
        # acos即为arccos
        # 向量点积 = cos * 向量1模 * 向量2模
        angle_ = math.degrees(math.acos((v1_x*v2_x+v1_y*v2_y)/(((v1_x**2+v1_y**2)**0.5)*((v2_x**2+v2_y**2)**0.5))))
        v3_x = v2[0] - v1[0]
        if v3_x < 0:
            angle_ = - 1 * angle_
            angle_ = angle_ + 360
    except:
        angle_ =65535.
    return angle_

